#include <stdio.h>

int main(){
	
	int grau = 100;
	
	printf("Variavel int(%%d): = %d\n",grau);
	printf("Variavel int(%%i): = %i\n",grau);
	printf("Variavel int(%%x): = %x\n",grau);
	printf("Variavel int(%%o): = %o\n",grau);
	printf("\n");
	
	double grau2 = 800.2345678;
	
	printf("Variavel double(%%f) = %f\n",grau2);
	printf("Variavel double(%%e) = %e\n",grau2);
	printf("Variavel double(%%g) = %g\n",grau2);
	printf("Variavel double(%%a) = %a\n",grau2);
	
	
	return 0;
}



